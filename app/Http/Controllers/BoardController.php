    {
        $board = new Board();
        $board->name = $request->data['name'];
        $board->public = 0;
        $board->user_id = $request->data['user_id'];
        $board->category_id = 1; // Ã  enlever
        $board->save();

        return response()->json([
            'message' => 'Successfully created a new board'
        ], 201);
    }

